// main concept 
for (var main = 0; main <= 10; main++) {
    console.log(main);
}
// odd number 
for (var i = 1; i <= 20; i += 2) {
    console.log(i);    
}
// even number 
for (var i = 0; i <= 20; i += 2) {
    console.log(i);
}